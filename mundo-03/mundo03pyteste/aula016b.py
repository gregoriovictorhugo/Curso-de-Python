a = (2, 5, 4)
b = (5, 8, 1, 2)

print(a)
print(b)

c = a + b
print(c)
print(len(c))# Conta quantas variáveis existem na tupla c
print(c.count(5))# Mostra quantas vezes o 5 aparece na tupla c
print(c.index(8))# Mostra em qual posição na tupla se encontra o 8
print(c.index(5, 1))# Mostra onde se encontra a variável  iniciando da posição 1

